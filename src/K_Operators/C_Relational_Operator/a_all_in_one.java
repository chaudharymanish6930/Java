package K_Operators.C_Relational_Operator;

public class a_all_in_one {
    public static void main(String[] args) {
        int x = 20, y = 15;

        System.out.println("x == y: " + (x == y));  //false
        System.out.println("x != y: " + (x != y));  // trrue
        System.out.println("x > y: " + (x > y));   // true
        System.out.println("x < y: " + (x < y));   // false
        System.out.println("x >= y: " + (x >= y)); // true
        System.out.println("x <= y: " + (x <= y));  // false
    }
}
