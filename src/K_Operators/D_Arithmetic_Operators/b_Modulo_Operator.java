package K_Operators.D_Arithmetic_Operators;

public class b_Modulo_Operator {
    public static void main(String[] args) {
        double x=5/2;
        System.out.println(x); //  2
        double y=5.0/2;
        System.out.println(y);  //2.5
        double z =5.0/2.0;
        System.out.println(z);
        double w = 5/2.0;
        System.out.println(w);
    }
}
