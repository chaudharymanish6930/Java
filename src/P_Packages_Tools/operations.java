package P_Packages_Tools;

public class operations {
    public int sum(int a,int b){
        return a+b;
    }
    public int subs(int a, int b){
        return  a-b;
    }
    public int multi(int a, int b){
        return a*b;
    }
    public double divi(int a, int b){
        return a/b;
    }
}
