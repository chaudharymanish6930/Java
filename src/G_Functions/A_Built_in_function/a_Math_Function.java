package G_Functions.A_Built_in_function;

public class a_Math_Function {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(64));
        System.out.println(Math.cbrt(27));
        double d = 98.6*98.6*98.6;
        System.out.println(Math.cbrt(d));
        System.out.println(Math.PI);
        System.out.println(Math.abs(-34));
        System.out.println(Math.floor(6.4));
        System.out.println(Math.floor(7.8));
        System.out.println(Math.ceil(3.4));
        System.out.println(Math.ceil(4.7));
        System.out.println(Math.min(3,7));
        System.out.println(Math.max(4.5,8.9));
        System.out.println(Math.pow(2,4));
        System.out.println(Math.pow(0,0));
    }
}
