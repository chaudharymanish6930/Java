package M_OOPs_in_Java.B_constructors;

public class c_default_constructor {
    int i;
    String s;

    // this is default constructors
    public static void main(String [] args){
        c_default_constructor  ab=new c_default_constructor();
        System.out.println(ab.i); //0
        System.out.println(ab.s);
    }
}
