package H_Arrays.A_1D_Array;

public class b_in_loops {
    public static void main(String[]args){
        int[] marks = new int[3];
        marks[0]=19;
        marks[1]=20;
        marks[2]=30;
        for(int i=0;i<3;i++){
            System.out.println(i+"="+marks[i]);
        }
    }
}
